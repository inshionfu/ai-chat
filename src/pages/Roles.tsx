        const { scrollTop, scrollHeight, clientHeight } = descriptionRef;
        // Ê£ÄÊµãÊòØÂê¶Êé•ËøëÂ∫ïÈÉ®
        const scrollRatio = scrollTop / (scrollHeight - clientHeight);
        
