  const [descriptionRef, setDescriptionRef] = useState<HTMLDivElement | null>(null);
  // Ê†πÊçÆÊèèËø∞ÈïøÂ∫¶Âà§Êñ≠ÊòØÂê¶ÈúÄË¶ÅÁâπÊÆäÂ§ÑÁêÜ
